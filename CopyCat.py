import turtle
turtle.bgcolor('purple')
x=turtle.Turtle()
x.color('white')
x.shape('circle')
x.stamp()
x.pensize(5)
x.goto(0,150)
x.goto(0,0)
y=turtle.Turtle()
y.pencolor('yellow')
y.pensize(5)
y.penup()
y.goto(0,150)
y.pendown()
y.goto(75,150)
y.goto(75,0)
g=turtle.Turtle()
g.color('green')
g.pensize(5)
g.penup()
g.goto(75,0)
g.pendown()
g.goto(75,-150)
turtle.mainloop()
